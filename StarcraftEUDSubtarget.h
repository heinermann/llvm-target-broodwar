#ifndef LLVM_LIB_TARGET_NIOS2_NIOS2SUBTARGET_H
#define LLVM_LIB_TARGET_NIOS2_NIOS2SUBTARGET_H

#include "StarcraftEUDFrameLowering.h"
#include "StarcraftEUDISelLowering.h"
#include "StarcraftEUDInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetFrameLowering.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"

#define GET_SUBTARGETINFO_HEADER
#include "StarcraftEUDGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class StarcraftEUDTargetMachine;

class StarcraftEUDSubtarget : public StarcraftEUDGenSubtargetInfo {
  virtual void anchor();

public:
  // StarcraftEUD R2 features
  // Bit manipulation instructions extension
  bool HasBMX;
  // Code Density instructions extension
  bool HasCDX;
  // Multi-Processor instructions extension
  bool HasMPX;
  // New mandatory instructions
  bool HasR2Mandatory;

protected:
  Triple TargetTriple;

  StarcraftEUDInstrInfo InstrInfo;
  StarcraftEUDTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;
  StarcraftEUDFrameLowering FrameLowering;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  StarcraftEUDSubtarget(const Triple &TT, const std::string &CPU,
                 const std::string &FS, const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  StarcraftEUDSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

  const StarcraftEUDInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const StarcraftEUDRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const StarcraftEUDTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};
} // namespace llvm

#endif
